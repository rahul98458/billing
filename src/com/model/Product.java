package com.model;

public class Product {
private int id;
private String name;
private int quantityavailabe;
private int mrp;
private int quantityadded;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getQuantityavailabe() {
	return quantityavailabe;
}
public void setQuantityavailabe(int quantityavailabe) {
	this.quantityavailabe = quantityavailabe;
}
public int getMrp() {
	return mrp;
}
public void setMrp(int mrp) {
	this.mrp = mrp;
}
public int getQuantityadded() {
	return quantityadded;
}
public void setQuantityadded(int quantityadded) {
	this.quantityadded = quantityadded;
}

}
